package slipdays

import (
	"reflect"
	"testing"
)

func TestCrossProduct(t *testing.T) {
	a := []map[int]int{{1: 1}, {2: 2}, {3: 3}}
	b := []map[int]int{{4: 4}, {5: 5}, {6: 6}}
	c := []map[int]int{{7: 7}, {8: 8}, {9: 9}}
	expected := [][]map[int]int{
		{{1: 1}, {4: 4}, {7: 7}},
		{{1: 1}, {4: 4}, {8: 8}},
		{{1: 1}, {4: 4}, {9: 9}},
		{{1: 1}, {5: 5}, {7: 7}},
		{{1: 1}, {5: 5}, {8: 8}},
		{{1: 1}, {5: 5}, {9: 9}},
		{{1: 1}, {6: 6}, {7: 7}},
		{{1: 1}, {6: 6}, {8: 8}},
		{{1: 1}, {6: 6}, {9: 9}},
		{{2: 2}, {4: 4}, {7: 7}},
		{{2: 2}, {4: 4}, {8: 8}},
		{{2: 2}, {4: 4}, {9: 9}},
		{{2: 2}, {5: 5}, {7: 7}},
		{{2: 2}, {5: 5}, {8: 8}},
		{{2: 2}, {5: 5}, {9: 9}},
		{{2: 2}, {6: 6}, {7: 7}},
		{{2: 2}, {6: 6}, {8: 8}},
		{{2: 2}, {6: 6}, {9: 9}},
		{{3: 3}, {4: 4}, {7: 7}},
		{{3: 3}, {4: 4}, {8: 8}},
		{{3: 3}, {4: 4}, {9: 9}},
		{{3: 3}, {5: 5}, {7: 7}},
		{{3: 3}, {5: 5}, {8: 8}},
		{{3: 3}, {5: 5}, {9: 9}},
		{{3: 3}, {6: 6}, {7: 7}},
		{{3: 3}, {6: 6}, {8: 8}},
		{{3: 3}, {6: 6}, {9: 9}},
	}
	cross := crossProduct(a, b, c)
	if !reflect.DeepEqual(cross, expected) {
		t.Fail()
	}
}
